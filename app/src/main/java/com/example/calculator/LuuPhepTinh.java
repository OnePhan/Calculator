package com.example.calculator;

public class LuuPhepTinh {
    private int id;
    private String so;
    public LuuPhepTinh(int id, String so) {
        this.id = id;
        this.so = so;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSo() {
        return so;
    }

    public void setSo(String so) {
        this.so = so;
    }
}
